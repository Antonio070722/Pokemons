public class EjerciciosString {


    public static void mostrarCadena(String cadena){
        System.out.println(cadena);
    }

    public static void mostrarLongitudCandea(String cadena){

        System.out.println("La longitud de tu cadena es: " + cadena.length());

    }

    public static void mostrarCaracterEnPosicion(String cadena, int posicion){

        System.out.println("El caracter es: " + cadena.charAt(posicion) + " numero " + posicion +" de la cadena " + cadena);

    }





}
